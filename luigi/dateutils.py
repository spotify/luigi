from datetime import datetime
from datetime import timedelta
from datetime import tzinfo

ZERO = timedelta(0)

# A UTC tzinfo class.

class UTC(tzinfo):
    """UTC"""

    def utcoffset(self, dt):
        return ZERO

    def tzname(self, dt):
        return "UTC"

    def dst(self, dt):
        return ZERO

utc = UTC()

def utcnow():
    return datetime.now(utc)
